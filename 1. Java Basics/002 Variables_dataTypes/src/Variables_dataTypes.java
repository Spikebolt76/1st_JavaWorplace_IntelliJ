public class Variables_dataTypes {

    public static void main(String[] args) {

        int x;  //Declaration
        x = 101; //Assignment

        int y = 102; //Initialization = Declaration + Assignment

        //Datatypes:-

        byte a = 10;
        System.out.println("byte a= "+a);

        short b = 203;
        System.out.println("short b= "+b);
        int c = 3222;
        System.out.println("int c= "+c);
        long d = -3423778634111103403L;
        System.out.println("long d= "+d);
        float e = 3.14f;
        System.out.println("float e= "+e);
        double f = 3.14;
        System.out.println("double f= "+f);
        char g = '&';
        System.out.println("char g= "+g);
        boolean h = true;
        System.out.println("boolean h= "+h);

        String i = "Jinesh Savaliya";
        System.out.println("String i= "+i);

    }
}